int		ft_sqrt(int nb)
{
	int		i;
	int		j;
	int		k;

	i = 3;
	j = 1;
	k = 1;
	while (k <= nb)
	{
		if (k == nb)
			return (j);
		k += i;
		i += 2;
		j++;
	}
	return (0);
}
